#  Crie um script em linguagem Python que leia três números e mostre o maior e o menor
# deles.



n1 = float(input(" 1 numero = "))
n2 = float(input(" 2 numero = "))
n3 = float(input(" 3 numero = "))

print(max(n1,n2,n3),"Este é o numero mais alto entre os tres")
print(min(n1,n2,n3),"Este é o numero mais baixo entre os tres")

