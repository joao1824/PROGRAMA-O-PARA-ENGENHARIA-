# Criar um script em linguagem Python que pergunte quanto você ganha por hora e o
# número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido
# mês.

hora = float(input("Quantos ganha por hora "))
horatr = float(input("Quantas horas trabalha "))

sal_bruto = hora * horatr

print(f"Seu salario bruto é {sal_bruto}")

